package javapractice1;

public class Lec3forloop {

	public static void main(String[] args) {
		//print value 0-9
		for (int i = 0; i <9; i++) {
			System.out.println(i);
		}
		//print value 5 to 20
		for (int i = 5; i <=20; i++) {
			System.out.println(i);
		}
		//print even number from 2 to 21
		for (int i=0; i<21; i=i+2) {
			System.out.println(i);
		}
		}

	}


